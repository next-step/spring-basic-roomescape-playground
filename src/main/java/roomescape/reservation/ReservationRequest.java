package roomescape.reservation;

public class ReservationRequest {
    private String name;
    private String date;
    private String theme;
    private String time;

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

    public String getTheme() {
        return theme;
    }

    public String getTime() {
        return time;
    }

    public void setName(String name) {
        this.name = name;
    }
}
